package variable_manipulation;

public class Demo3 {
    public static void main(String[] args){
        double a=4.5;
        double b=6.5;

        double sum=a+b;

        System.out.println("sum of a and b" +sum);

        double sub=a-b;

        System.out.println("sub of a and b"+sub);

        double multi=a*b;

        System.out.println("multi of a and b"+multi);

        double divide=a/b;

        System.out.println("divide of a and b"+divide);
    }
}
